class Main {
  public static void main(String[] args) {
    int[] arr = {1, 5, 1, 10, 13, 9, 40, 33, 28, 15};
    
    System.out.print("Unsorted: ");
    for (int n : arr) {
      System.out.print(n + " ");
    }
    System.out.println();

    
    System.out.print("Sorted:   ");
    for (int n : arr) {
      System.out.print(n + " ");
    }
    System.out.println();

  }
}